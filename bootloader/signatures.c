static uint8_t sigIndexes[NUMBER_OF_SIGNATURES] = {0, 0, 0};
static uint8_t signatures[NUMBER_OF_SIGNATURES][SIGNATURE_LENGHT];



//********************************
//
// Protobuf Schema for reference
// Request
// message MsgSetFirmwareSignature
// {
//    required uint32 Version	    = 1;
//    required bytes  Indexes     = 2;     
//    required bytes  Signatures  = 3;  
// }
// 
// Response (Actually this is an Ack msg)
// message MsgSetFirmwareSignatureAck
// {
//    required uint32  SigCount = 1;
// }
//********************************
bool SignatureSet( unsigned char* d, sSigResponse* res)
{
  //! The first byte is Protobuf Field Number + type
  //! for version, the first byte should be 0x08 which means Field: 1, type: varint

  if(*d++ != 0x08)
  {
    res->response[0] = SIG_ERR_PROTOBUF;
    return false;
  }

  //! Version
  uint8_t version = *d++;

  //! Indexes
  //! Field: 2, type: Length-delimited
  if(*d++ != 0x12)
  {
    res->response[0] = SIG_ERR_PROTOBUF;
    return false;
  }

  //! 3 signature index needed
  if( *d++ != NUMBER_OF_SIGNATURES )
  {
    res->response[0] = SIG_ERR_INDEX;
    return false;
  }

  //! Read sigIndex
  sigIndexes[0] = *d++;
  sigIndexes[1] = *d++;
  sigIndexes[2] = *d++;

  //! Validate indexes
  if( sigIndexes[0] < 1 || sigIndexes[0] > PUBKEYS || 
      sigIndexes[1] < 1 || sigIndexes[1] > PUBKEYS || 
      sigIndexes[2] < 1 || sigIndexes[2] > PUBKEYS || 
      sigIndexes[0] == sigIndexes[1] || 
      sigIndexes[0] == sigIndexes[2] ||
      sigIndexes[1] == sigIndexes[2])
  {
    res->response[0] = SIG_ERR_INDEX;
    return false;
  }

  //! Field: 3, type: Length-delimited
  if(*d++ != 0x1A)
  {
    res->response[0] = SIG_ERR_PROTOBUF;
    return false;
  }

  //! Signature lenght is 64x3 = 192 bytes
  //! C001 => (VarInt) => 1100 0000 = 192
  if(*d++ != 0xC0 || *d++ != 0x01)
  {
    res->response[0] = SIG_ERR_LEN;
    return false;
  }

  //! Copy signatures
  memcpy(signatures[0], d, SIGNATURE_LENGHT);
  memcpy(signatures[1], d+64, SIGNATURE_LENGHT);
  memcpy(signatures[2], d+128, SIGNATURE_LENGHT);

  res->response[0] = 0x80;
  res->response[1] = 0x03;
  res->len = 2;

  return true;
}

uint32_t    SignatureCheck( const uint8_t* hash )
{
  if(sigIndexes[0] == 0) { 
    return SIG_FAIL;
  }

  //! Check first signature
  if (ecdsa_verify_digest(&secp256k1, pubkey[sigIndexes[0] - 1], (const uint8_t *)signatures[0], hash) != 0) 
  { 
    return SIG_FAIL;
  }

  //! Check second signature
  if (ecdsa_verify_digest(&secp256k1, pubkey[sigIndexes[1] - 1], (const uint8_t *)signatures[1], hash) != 0) 
  { 
    return SIG_FAIL;
  }

  //! Check third signature
  if (ecdsa_verify_digest(&secp256k1, pubkey[sigIndexes[2] - 1], (const uint8_t *)signatures[2], hash) != 0) 
  { 
    return SIG_FAIL;
  }

  return SIG_OK;
}


